let shuffle = () => {
  let currentIndex = text.length;
  let randomIndex;
  while(currentIndex != 0){
    randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex--;
    [text[currentIndex], text[randomIndex]] = [text[randomIndex], text[currentIndex]]
  }
  return text 
}


